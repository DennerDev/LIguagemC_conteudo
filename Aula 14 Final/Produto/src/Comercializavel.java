/* INTERFACES
 * As interfaces são muito utilizadas no desenvolvimento em java, elas
 * obrigam que um determinado grupo de classes tenham métodos e 
 * propriedades em comum para existir em um determinado contexto, contudo
 * os métodos podem ser implementados em cada classe de uma maneira
 * diferente.
 * */
public interface Comercializavel {
	public void entregar();
	public void receber();
}
