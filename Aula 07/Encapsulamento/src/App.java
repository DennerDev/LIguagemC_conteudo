/**
 * Encapsulamento
 * 
Encapsular os dados de uma aplicação significa evitar que estes sofram acessos indevidos.
Public: Significa visibilidade total, ou seja, o elemento é enxergado na própria classe que foi
criado e em outras classes desde que esteja no mesmo pacote.

Private: O elemento tem a sua visibilidade restrita a classe que ele está. Outras classes
não podem acessar esses elementos diretamente.

Protected: Permite que os atributo ou método seja visualizado na própria classe e em suas
classes filhas (assunto de herança)
 */
